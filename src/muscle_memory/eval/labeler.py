"""Interactive labeling for skill credit assignments.

The question: "This skill fired and the heuristic credited it.
Was the credit deserved — did the skill actually help?"

This is more specific and faster than labeling entire sessions.
Each decision takes 3-5 seconds because you can see the skill,
what it told the agent to do, and whether that matched the task.
"""

from __future__ import annotations

import re
import uuid
from dataclasses import dataclass

from rich.console import Console
from rich.panel import Panel
from rich.rule import Rule

from muscle_memory.db import Store
from muscle_memory.eval import EvalLabel
from muscle_memory.models import Episode, Skill

console = Console()

# ── Text cleaning ──────────────────────────────────────────────────

_XML_TAG_RE = re.compile(r"<[^>]+>")
_NOISE_PATTERNS = [
    re.compile(r"Caveat:.*?unless the user explicitly asks you to\.\s*", re.S),
    re.compile(r"DO NOT respond to these messages.*?$", re.M),
    re.compile(r"The messages below were generated by the user.*?\.\s*"),
    re.compile(r"explicitly asks you to\.?\s*"),
    re.compile(r"\[\d+m"),
    re.compile(r"^/\w+\s*$", re.M),
    re.compile(r"^.*[┏┓┗┛┃┡┣┫┻━╇╋╭╮╯╰│─┳┴┼└┘┐┌]+.*$", re.M),
    re.compile(r"^\s*(?:model|help)\s*$", re.M),
    re.compile(r"Set model to.*?effort\s*"),
]


def _clean(text: str) -> str:
    text = _XML_TAG_RE.sub("", text)
    for pat in _NOISE_PATTERNS:
        text = pat.sub("", text)
    return re.sub(r"\s{2,}", " ", text).strip()


# ── Credit assignment ──────────────────────────────────────────────


@dataclass
class CreditEvent:
    """A single skill activation in a session that received credit."""

    episode: Episode
    skill: Skill
    heuristic_outcome: str  # what the heuristic said about this session


def _get_unlabeled_credits(store: Store, limit: int = 30) -> list[CreditEvent]:
    """Find skill activations that haven't been labeled yet.

    Deduplicates by (session, skill) — only the latest episode per session.
    Sorted by uncertainty: UNKNOWN sessions first, then FAILURE, then SUCCESS.
    """
    all_unlabeled = store.list_unlabeled_episodes(limit=limit * 5)

    # Deduplicate episodes by session (keep latest)
    by_session: dict[str, Episode] = {}
    for ep in all_unlabeled:
        sid = ep.session_id or ep.id
        existing = by_session.get(sid)
        if existing is None or ep.trajectory.num_tool_calls() > existing.trajectory.num_tool_calls():
            by_session[sid] = ep

    # Expand to credit events: one per (episode, skill)
    events: list[CreditEvent] = []
    seen_labels: set[str] = set()

    # Pre-fetch existing credit labels to skip already-labeled pairs
    existing_labels = store.get_eval_labels("credit")
    for lbl in existing_labels:
        seen_labels.add(f"{lbl.episode_id}:{lbl.skill_id}")

    for ep in by_session.values():
        if not ep.activated_skills:
            continue
        for sid in ep.activated_skills:
            if f"{ep.id}:{sid}" in seen_labels:
                continue
            skill = store.get_skill(sid)
            if skill is None:
                continue
            events.append(CreditEvent(
                episode=ep,
                skill=skill,
                heuristic_outcome=ep.outcome.value,
            ))

    # Sort: UNKNOWN first (most valuable to label), then FAILURE, then SUCCESS
    priority = {"unknown": 0, "failure": 1, "success": 2}
    events.sort(key=lambda e: priority.get(e.heuristic_outcome, 3))
    return events[:limit]


# ── Main labeling loop ─────────────────────────────────────────────


def label_credits_interactive(store: Store, *, limit: int = 10) -> int:
    """Label skill credit assignments interactively."""
    events = _get_unlabeled_credits(store, limit=limit)

    if not events:
        console.print("[dim]No unlabeled credit assignments remaining.[/dim]")
        return 0

    total_credits = store.count_eval_labels("credit")
    console.print(
        Panel(
            f"[bold]Labeled so far:[/bold] {total_credits} credits\n"
            f"[bold]This batch:[/bold] {len(events)} to review\n\n"
            "[dim]For each: a skill fired and got a point.\n"
            "Did the skill actually help with the task?[/dim]\n\n"
            "  [green bold]y[/green bold] = yes, skill helped    "
            "[red bold]n[/red bold] = no, irrelevant or wrong\n"
            "  [bold]?[/bold] = skip    "
            "[bold]q[/bold] = quit",
            title="Credit Labeling",
        )
    )

    added = 0
    for i, event in enumerate(events, 1):
        _render_credit(event, i, len(events))

        while True:
            try:
                key = console.input(
                    f"\n  Skill got a [bold]{event.heuristic_outcome}[/bold] point. "
                    "Deserved? [green]y[/green]/[red]n[/red]/[bold]?[/bold]/[bold]q[/bold]: "
                ).strip().lower()
            except (EOFError, KeyboardInterrupt):
                console.print(f"\n[dim]Labeled {added} credits.[/dim]")
                return added

            if key == "q":
                console.print(f"[dim]Labeled {added} credits.[/dim]")
                return added
            if key == "?":
                console.print("[dim]  Skipped.[/dim]")
                break
            if key in ("y", "n"):
                human = "deserved" if key == "y" else "undeserved"
                store.add_eval_label(EvalLabel(
                    id=uuid.uuid4().hex[:16],
                    label_type="credit",
                    episode_id=event.episode.id,
                    skill_id=event.skill.id,
                    heuristic_outcome=event.heuristic_outcome,
                    human_outcome=human,
                ))
                added += 1
                color = "green" if key == "y" else "red"
                console.print(f"  [{color}]{human}[/{color}]")
                break

            console.print("[dim]  Press y/n/?/q[/dim]")

    console.print(f"\n[green]Done.[/green] Labeled {added} credits.")
    return added


# ── Rendering ──────────────────────────────────────────────────────


def _render_credit(event: CreditEvent, idx: int, total: int) -> None:
    """Render a credit event for labeling — compact, scannable."""
    from muscle_memory.cli import _format_outcome, _relative_time

    ep = event.episode
    skill = event.skill

    console.print()
    console.print(Rule(f"{idx}/{total}"))

    # The skill that fired
    console.print(
        Panel(
            f"[bold]When:[/bold] {skill.activation[:150]}\n\n"
            f"[bold]Do:[/bold] {skill.execution[:200]}",
            title=f"Skill {skill.id[:8]} ({skill.maturity.value}, score {skill.score:.2f})",
            border_style="cyan",
        )
    )

    # The context: what was the user doing?
    request = _get_user_request(ep)
    outcome_str = _format_outcome(ep.outcome)
    when = _relative_time(ep.started_at)

    console.print(
        f"  [dim]Session ({when}):[/dim] {request}\n"
        f"  [dim]Session outcome:[/dim] {outcome_str}"
    )


def _get_user_request(ep: Episode) -> str:
    """Extract the user's actual request."""
    fragments = []
    for raw in [ep.user_prompt, ep.trajectory.user_followup]:
        cleaned = _clean(raw)
        if cleaned:
            fragments.append(cleaned)
    combined = " ".join(fragments).strip()
    if not combined:
        return "(no prompt captured)"
    return combined[:120] + ("..." if len(combined) > 120 else "")
